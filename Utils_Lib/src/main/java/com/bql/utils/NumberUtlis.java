    public static String keepIntCount(int value, int count) {
        return String.format("%0" + count + "d", value);

    }

    /**
     * 保留小数位
     *
     * @param value 值
     * @param count 小数位
     * @return
     */
    public static float keepFloatCount(float value, int count) {
        BigDecimal b = new BigDecimal(value);
        float f1 = (float) b.setScale(count, BigDecimal.ROUND_HALF_UP)
                .doubleValue();
        return f1;
    }

    //地球半径
    private static final double EARTH_RADIUS = 6378137.0;

    /**
     * 获取两个坐标点的距离
     *
     * @param longitude1
     * @param latitude1
     * @param longitude2
     * @param latitude2
     * @return 米
     */
    public static double getDistance(double longitude1, double latitude1,
                                     double longitude2, double latitude2) {
        double Lat1 = rad(latitude1);
        double Lat2 = rad(latitude2);
        double a = Lat1 - Lat2;
        double b = rad(longitude1) - rad(longitude2);
        double s = 2 * Math.asin(Math.sqrt(Math.pow(Math.sin(a / 2), 2)
                + Math.cos(Lat1) * Math.cos(Lat2)
                * Math.pow(Math.sin(b / 2), 2)));
        s = s * EARTH_RADIUS;
        s = Math.round(s * 10000) / 10000;
        BigDecimal bg = new BigDecimal(s / 1000);
        return bg.setScale(2, BigDecimal.ROUND_HALF_UP).doubleValue();
    }

    private static double rad(double d) {
        return d * Math.PI / 180.0;
    }

